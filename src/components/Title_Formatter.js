export let Title_Formatter = (measure) => {
    switch (measure) {
        case "10green": 
            return "10Green";
            case "aqi": 
            return "AQI";
            case "arsenic": 
            return "Arsenic";
            case "cadmium": 
            return "Cadmium";
            case "co": 
            return "CO";
            case "lead": 
            return "Lead";
            case "nickel": 
            return "Nickel";
            case "no": 
            return "NO";
            case "no2": 
            return "NO2";
            case "ozone": 
            return "Ozone";
            case "pm10": 
            return "PM10";
            case "pm25": 
            return "PM25";
            case "so2": 
            return "SO2";
            

    }
    
}